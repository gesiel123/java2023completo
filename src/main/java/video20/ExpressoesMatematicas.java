package video20;

public class ExpressoesMatematicas {
	public static void main(String[] args) {
		System.out.println(3 + 2 * 4);
		System.out.println((3 + 2) * 4);
		System.out.println(60 / (3 + 2) * 4);
		System.out.println(60 / ((3 + 2) * 4));
		System.out.println("Mod: " + 14 % 3);
		System.out.println("Mod: " + 19 % 5);
	}

}
